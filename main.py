import argparse
import os
import sys

from dotenv import load_dotenv
from google import genai
from google.genai import types

from call_function import available_functions, call_function
from prompts import system_prompt


def main():
    load_dotenv()
    api_key = os.environ.get("GEMINI_API_KEY")
    if not api_key:
        raise RuntimeError("GEMINI_API_KEY not found")

    parser = argparse.ArgumentParser(description="Boot Agent")
    parser.add_argument("user_prompt", type=str, help="User prompt")
    parser.add_argument("--verbose", action="store_true", help="Enable verbose output")
    args = parser.parse_args()

    messages = [types.Content(role="user", parts=[types.Part(text=args.user_prompt)])]

    for _ in range(20):
        client = genai.Client(api_key=api_key)
        response = client.models.generate_content(
            model="gemini-2.5-flash",
            contents=messages,
            config=types.GenerateContentConfig(
                tools=[available_functions],
                system_instruction=system_prompt,
                temperature=0,
            ),
        )
        if not response.usage_metadata:
            raise RuntimeError(
                "Response has no usage_metadata. This may indicate a failed request."
            )
        if not response.candidates:
            raise RuntimeError("Response has no candidates...why?")

        for candidate in response.candidates:
            if candidate.content:
                messages.append(candidate.content)

        if args.verbose:
            # print(f"User prompt: {args.user_prompt}")
            print(f"Prompt tokens: {response.usage_metadata.prompt_token_count}")
            print(f"Response tokens: {response.usage_metadata.candidates_token_count}")

        function_results = []
        if response.function_calls:
            for call in response.function_calls:
                function_call_result = call_function(call, args.verbose)
                if not function_call_result.parts:
                    raise Exception("Malformed function call result - no parts")
                if not function_call_result.parts[0].function_response:
                    raise Exception("Empty response from function call")
                if not function_call_result.parts[0].function_response.response:
                    raise Exception("Empty result from function call response")
                function_results.append(function_call_result.parts[0])
                if args.verbose:
                    print(
                        f"-> {function_call_result.parts[0].function_response.response}"
                    )
            messages.append(types.Content(role="user", parts=function_results))
        else:
            print(response.text)
            return

    print(
        "Max iterations reached without a response without function calls, exiting to save tokens."
    )
    sys.exit(1)


if __name__ == "__main__":
    main()
